def _get_statecode_by_key(state_key: str) -> str | None:
    """Return the numeric statecode for a given short state key (e.g., 'MH' -> '27')."""
    key_upper = (state_key or "").strip().upper()
    for s in statedata:
        if s.get("key", "").upper() == key_upper:
            return s.get("statecode")
    return None


def get_stamp_duty(statecode: str, finance_amount: float) -> float | None:
    """
    Return the applicable stamp duty for a given statecode and finance (loan) amount.
    If no matching slab is found, return None.
    """
    if statecode is None:
        return None
    try:
        amt = float(finance_amount)
    except Exception:
        return None

    code = str(statecode).strip()
    for row in stampdutydata:
        if row.get("statecode") == code and row.get("minfinanceamount", float("-inf")) <= amt <= row.get("maxfinanceamount", float("inf")):
            return row.get("stampdutyamount")
    return None


def get_stamp_duty_by_key(state_key: str, finance_amount: float) -> float | None:
    """
    Convenience wrapper to look up by the 2-letter key (e.g., 'MH', 'DL').
    """
    code = _get_statecode_by_key(state_key)
    return get_stamp_duty(code, finance_amount)


def list_supported_states():
    """Return a sorted list of (statecode, key, name) that have at least one stamp duty slab."""
    present = {row["statecode"] for row in stampdutydata}
    out = []
    for s in statedata:
        if s.get("statecode") in present:
            out.append((s.get("statecode"), s.get("key"), s.get("name")))
    return sorted(out, key=lambda t: (t[0], t[1] or "", t[2] or ""))


